--1. SCRIPT COMPLETO ORACLE 19c
--Crear Tabla
CREATE TABLE EMPLEADOS_H (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(100),
    CARGO VARCHAR2(100),
    SALARIO NUMBER(10,2),
    FECHA_REGISTRO DATE DEFAULT SYSDATE
);

--2. TABLA LOG PARA EL TRIGGER
CREATE TABLE LOG_REGISTRO_50 (
    ID_LOG NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MENSAJE VARCHAR2(300),
    FECHA_LOG DATE DEFAULT SYSDATE
);

COMMIT;

--3. PROCEDIMIENTOS ALMACENADOS CRUD
--CREATE
CREATE OR REPLACE PROCEDURE SP_INSERTAR_EMPLEADO(
    P_NOMBRE IN VARCHAR2,
    P_CARGO IN VARCHAR2,
    P_SALARIO IN NUMBER
)
AS
BEGIN

    INSERT INTO EMPLEADOS_H(
        NOMBRE,
        CARGO,
        SALARIO
    )
    VALUES(
        P_NOMBRE,
        P_CARGO,
        P_SALARIO
    );

    COMMIT;

END;
/

COMMIT;

--READ
CREATE OR REPLACE PROCEDURE SP_LISTAR_EMPLEADOS(
    P_CURSOR OUT SYS_REFCURSOR
)
AS
BEGIN

    OPEN P_CURSOR FOR
    SELECT *
    FROM EMPLEADOS_H
    ORDER BY ID;

END;
/

COMMIT;

--UPDATE
CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_EMPLEADO(
    P_ID IN NUMBER,
    P_NOMBRE IN VARCHAR2,
    P_CARGO IN VARCHAR2,
    P_SALARIO IN NUMBER
)
AS
BEGIN

    UPDATE EMPLEADOS_H
    SET
        NOMBRE = P_NOMBRE,
        CARGO = P_CARGO,
        SALARIO = P_SALARIO
    WHERE ID = P_ID;

    COMMIT;

END;
/

COMMIT;

--DELETE
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_EMPLEADO(
    P_ID IN NUMBER
)
AS
BEGIN

    DELETE FROM EMPLEADOS_H
    WHERE ID = P_ID;

    COMMIT;

END;
/

COMMIT;

--4. TRIGGER PARA DETECTAR EL REGISTRO NUMERO 50
CREATE OR REPLACE TRIGGER TRG_REGISTRO_50
AFTER INSERT ON EMPLEADOS_H
FOR EACH ROW
BEGIN

    IF :NEW.ID = 50 THEN

        INSERT INTO LOG_REGISTRO_50(
            MENSAJE
        )
        VALUES(
            'Se insert¾ el registro n·mero 50 correctamente'
        );

    END IF;

END;
/

COMMIT;

--5. REGISTROS DE EJEMPLO
BEGIN

    FOR I IN 1..50 LOOP

        SP_INSERTAR_EMPLEADO(
            'Empleado ' || I,
            'Desarrollador',
            2000 + I
        );

    END LOOP;

END;
/

COMMIT;

